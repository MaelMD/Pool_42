int	alpha(char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if ((s[i] >= 'a' && s[i] <= 'z') || (s[i] >= 'A' && s[i] <= 'Z'))
		{
			i++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}

int	ft_str_is_alpha(char *str)
{
	if (alpha(str))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
