int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb == 0 || nb == 1)
		return (0);
	while (i < ((nb / 2) + 1))
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int	i;

	i = nb;
	if (nb < 2)
		return (2);
	if (nb > 2147483629 && nb <= 2147483647)
		return (2147483647);
	while (!ft_is_prime(i) && i < 2147483647)
		i++;
	return (i);
}
