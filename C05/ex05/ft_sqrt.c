int	ft_sqrt(int nb)
{
	int	i;
	int	max;

	i = 0;
	if (nb == 1)
		return (1);
	if (nb / 2 < 50000)
		max = nb / 2;
	else
		max = 50000;
	while (i <= max)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
