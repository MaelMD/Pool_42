#include <unistd.h>

void	ft_is_negative(int n)
{
	char	pon;

	if (n < 0)
	{
		pon = 'N';
		write(1, &pon, 1);
	}
	else
	{
		 pon = 'P';
		 write(1, &pon, 1);
	}
}
